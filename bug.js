import React, { useState, useEffect } from 'react';

function MyComponent() {
  const [count, setCount] = useState(0);

  useEffect(() => {
    // This effect runs after every render, including the initial render.
    console.log('Effect running:', count);
    // Potential for infinite loop if count is modified within the effect
    setCount(count + 1); // modify state inside effect 
  }, [count]); // This dependency array is incorrect. It causes infinite loop 

  return (
    <div>
      <p>Count: {count}</p>
      <button onClick={() => setCount(count + 1)}>Increment</button>
    </div>
  );
}
export default MyComponent;
